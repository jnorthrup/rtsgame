/**
 * Static definition for a type of unit or building.
 * Contains base stats, abilities, model info, etc.
 * Origin: 'retired/config/unitTypes.ts', 'rtsgame-kmp/shared/entity/Unit.kt' (UnitType enum).
 */
data class UnitTypeDefinition(
    val id: String, // e.g., "tank", "worker", "command_center"
    val name: String, // Display name
    val modelId: String, // Reference to 3D model (from model-manifest.json)
    val baseMaxHp: Float,
    val baseSpeed: Float,
    val baseArmor: Float,
    val baseArmorType: ArmorType = ArmorType.MEDIUM,
    // Attack properties
    val baseAttackDamage: Float,
    val baseAttackRange: Float,
    val baseAttackCooldown: Float,
    val baseDamageType: DamageType = DamageType.NORMAL,
    val projectileModelId: String? = null, // For visible projectiles
    // Resource properties
    val cost: Map<ResourceType, Int> = emptyMap(),
    val providesSupply: Int = 0,
    val requiresSupply: Int = 0,
    // Abilities
    val abilities: List<String> = emptyList(), // List of ability IDs
    // Production
    val canProduce: List<String> = emptyList() // List of UnitTypeDefinition IDs it can produce
    // Add other relevant static properties: visionRange, energy, etc.
)

