// --- I. Core Entity Component System (ECS) Abstractions ---

/**
 * Represents a unique entity in the game world.
 * In a full ECS, this might just be an ID, with components holding all data.
 * Origin: General ECS pattern, inspired by component structures in 'retired/' and 'rtsgame-kmp/'.
 */
typealias EntityId = String // Or value class EntityId(val id: String)

/**
 * Base interface for all components that can be attached to an entity.
 * Components should primarily be data containers.
 * Origin: General ECS pattern.
 */
interface Component

/**
 * Manages entities and their components.
 * Origin: General ECS pattern.
 */
interface EntityManager {
    fun createEntity(vararg components: Component): EntityId
    fun destroyEntity(entityId: EntityId)
    fun addComponent(entityId: EntityId, component: Component)
    fun <T : Component> removeComponent(entityId: EntityId, componentType: kotlin.reflect.KClass<T>)
    fun <T : Component> getComponent(entityId: EntityId, componentType: kotlin.reflect.KClass<T>): T?
    fun getAllEntitiesWithComponent(componentType: kotlin.reflect.KClass<out Component>): List<EntityId>
    // Consider: fun <T1: Component, T2: Component> getAllEntitiesWithComponents(c1Type: KClass<T1>, c2Type: KClass<T2>): List<EntityId>
}

/**
 * Base interface for game systems that operate on entities with specific components.
 * Systems contain the logic that transforms component data.
 * Origin: General ECS pattern, 'retired/core/systems/', 'rtsgame-kmp/shared/systems/'.
 */
interface GameSystem {
    /**
     * Called every game tick to update the system's state and entities.
     * @param gameState The current, possibly read-only, game state.
     * @param entityManager For querying/modifying entities and components.
     * @param deltaTime Time elapsed since the last tick.
     * @return Potentially a list of events or commands generated by the system.
     */
    fun update(entityManager: EntityManager, deltaTime: Float /*, eventBus: EventBus? */)
    // Consider adding: fun initialize(entityManager: EntityManager, eventBus: EventBus?)
}

